def backtest_strategy(strategy):
    # Create a cerebro entity
    cerebro = bt.Cerebro()

    # Add a strategy
    cerebro.addstrategy(strategy)

    # Datas are in a subfolder of the samples. Need to find where the script is
    # because it could have been called from anywhere
    modpath = os.path.dirname(os.path.abspath(sys.argv[0]))
    datapath = os.path.join(modpath, './datas/orcl-1995-2014.txt')

    # Create a Data Feed
    data = bt.feeds.YahooFinanceCSVData(
        dataname=datapath,
        # Do not pass values before this date
        fromdate=datetime.datetime(2000, 1, 1),
        # Do not pass values after this date
        todate=datetime.datetime(2000, 12, 31),
        # Do not use adj close price for executed price
        adjclose=False,
        reverse=False)

    # Add the Data Feed to Cerebro
    cerebro.adddata(data)

    # Set our desired cash start
    cerebro.broker.setcash(1000.0)

    # Add a FixedSize sizer according to the stake
    cerebro.addsizer(bt.sizers.FixedSize, stake=10)

    # Set the commission
    cerebro.broker.setcommission(commission=0.0)

    # Run over everything
    cerebro.run()

    cerebro.plot()


CLASSES_MAP = {
    'QuickstartStrategy': QuickstartStrategy,
    'BuyAndHold': BuyAndHold,
}


def parse_args():
    parser = argparse.ArgumentParser(description='Common Strategies Sample')
    parser.add_argument('--strategy', '-s',
                        choices=list(CLASSES_MAP),
                        default='BuyAndHold',
                        help='strategy to be used during backtesting')
    return parser.parse_args()


if __name__ == '__main__':
    args = parse_args()
    input_strategy = CLASSES_MAP[args.strategy]
    backtest_strategy(input_strategy)
